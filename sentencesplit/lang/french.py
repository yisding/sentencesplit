from sentencesplit.lang.common import Common, Standard


class French(Common, Standard):
    iso_code = "fr"

    # French overrides zero scan methods. Its only language-specific hook is
    # elision ("l'art.", "d'env."), and that flows automatically: the
    # Abbreviation class sets ELISION_CHARACTERS, so _AbbreviationData folds the
    # apostrophes into ``boundary_class`` and exposes ``elision_chars``, which the
    # PeriodClassifier reads off the SAME data (boundary lookbehind +
    # _elision_strip). So French rides the base PeriodClassifier (BASE_POLICY)
    # inherited from Standard directly — identical shape to Italian. French is NOT
    # one of the CAPITALIZED_FOLLOWER_IS_BOUNDARY_CUE languages (it capitalizes
    # proper nouns mid-sentence), so that flag stays off and capital followers
    # flow through the split-mode ambiguity dial.

